class Kanye {
    constructor(quote){
        this.KanyeWis = quote;
    }
    speakDestiny(){
        return this.KanyeWis;
    }
    shoutDestiny(){
        return this.KanyeWis.toUpperCase() + "!!!!!!";
    }
    refuseDestiny(){
        return this.KanyeWis + "...I recant my foolishness! SAVE ME OH LAWD!";
    }
}

